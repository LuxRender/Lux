#ifndef LUX_RANDOM_H
#define LUX_RANDOM_H

#define MEXP 19937
#include "./SFMT/SFMT.h"

namespace lux
{


namespace random
{


	inline void init()
	{
		init_gen_rand(1234);
	}

	inline float floatValue()
	{
		return genrand_real2();
	}
		
	inline unsigned long uintValue()
	{
		return gen_rand32();
	}
}

}

#endif //LUX_RANDOM_H

